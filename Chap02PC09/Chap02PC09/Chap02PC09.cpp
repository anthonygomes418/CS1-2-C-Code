// Cyborg Data Types
// This program will show how much data can be stored in each data type.

#include<iostream>
using namespace std;

int main()
{
	
	cout <<" The size of a char is " << sizeof(char);
	cout <<" bytes.\n";
	cout <<" The size of a int is " << sizeof(int);
	cout <<" bytes.\n";
	cout <<" The size of a float is " << sizeof(float);
	cout <<" bytes.\n";
	cout <<" The size of a double is " << sizeof(double);
	cout <<" bytes.\n";

    system("PAUSE");
    return 0;
}